package day04;

public class Test27 {

	public static void main(String[] args) {
		/*
		변수 = 초기값;
		while(조건식){
			실행문들 ....;
			증감식;
		}
		 */
		// 5번 반복
		int a = 90;    //초기식
		while(a <= 100) {  // 조건식
			System.out.println("hello world"); //반복해서 실행할 문장
			a++;  //증감식
			
		}
		
		
		
		//0~5 까지 출력 0 1 2 3 4 5
		
		int n = 0;
		while(n < 6 ) {
			System.out.println(n);
			//증감식 뺴먹으면 무한반복한다.
			n++;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
